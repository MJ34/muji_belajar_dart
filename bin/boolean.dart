// * BOOLEAAN*
/* 
1. Boolean adalah tipe data yang hanya memiliki dua nilai, yaitu benar atau salah
2. Boolean direpresentasikan dengan kata kunci bool
3. Untuk nilai benar, menggunakan kata kunci true
4. Untuk nilai salah, menggunakan kata kunci false */

void main() {
  bool finish = false;
  print(finish);

  finish = true;
  print(finish);
}
