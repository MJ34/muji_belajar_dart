// *********** Operator Perbandingan ****************
/* 
1. Operator perbandingan adalah operator yang hasilnya adalah bool
2. Operator perbandingan bisa digunakan di tipe data number dan string
 */

void main(){

  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  print('muji' == 'muji');
  print('muji' != 'muji');

}