// ********** Operator Aritmatika ***************
/* 
1. + Pertambahan
2. - , -expression Pengurangan atau Negatif
3. * Perkalian
4. / Pembagian, Hasil double
5. ~/ Pembagian, Hasil int
6. % Sisa Bagi
 */

void main(){

  var first = 10;
  var second = 3;

  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second);
  print(first ~/ second);
  print(first % second);

}
